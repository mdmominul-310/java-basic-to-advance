package loop;

public class Multiplication {
    public static void main(String[] args){
  // Write a program that prints the multiplication table from 1 to 5

        for(int i=1;i<=5;i++){
            for(int j=1;j<=10;j++){
                System.out.print(i*j);
                System.out.print(" ");
            }
            System.out.println("");
        }



    }
}
